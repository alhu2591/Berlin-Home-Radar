import type { SourceDraft } from '@/types/source';

export type SourceStatus = 'active' | 'worker_ready';
export type SourceCategory = 'portal' | 'marketplace' | 'room' | 'agency' | 'public_housing' | 'student' | 'furnished';
export type SourceMarket = 'DE';
export type WorkerStrategy = 'auto' | 'official_api' | 'public_html' | 'rss';

export interface SourceCatalogEntry {
  id: string;
  label: string;
  market: SourceMarket;
  country: string;
  defaultCity: string;
  defaultListingType: SourceDraft['listingType'];
  defaultMaxResults: string;
  executionMode: SourceDraft['executionMode'];
  workerPath: string;
  description: string;
  category: SourceCategory;
  status: SourceStatus;
  cities: string[];
  supportsDemo: boolean;
  supportsWorker: boolean;
  enabledByDefault: boolean;
  workerStrategyHint: WorkerStrategy;
  defaultConfig: Record<string, string>;
  docsUrl?: string;
}

function entry(input: Omit<SourceCatalogEntry, 'market' | 'country' | 'defaultCity' | 'cities'> & { country?: string; defaultCity?: string; cities?: string[] }) : SourceCatalogEntry {
  return {
    market: 'DE',
    country: input.country ?? 'DE',
    defaultCity: input.defaultCity ?? 'Berlin',
    cities: input.cities ?? ['Berlin'],
    ...input,
  };
}

