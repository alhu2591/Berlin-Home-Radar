export const adapterCatalog = sourceCatalog.map((item) => ({
  id: item.id,
  label: item.label,
  defaultCity: item.defaultCity,
}));

export function getSourceCatalogEntry(adapterId: string) {
  return sourceCatalog.find((item) => item.id === adapterId);
}

export function buildDraftFromCatalog(adapterId: string): SourceDraft {
  const entry = getSourceCatalogEntry(adapterId) ?? sourceCatalog[0]!;
  return {
    name: `${entry.label} ${entry.defaultCity}`,
    adapterId: entry.id,
    city: entry.defaultCity,
    listingType: entry.defaultListingType,
    maxResults: entry.defaultMaxResults,
    country: entry.country,
    executionMode: entry.executionMode,
    workerPath: entry.workerPath,
    fetchStrategy: (entry.defaultConfig.fetchStrategy as SourceDraft['fetchStrategy']) ?? entry.workerStrategyHint,
    district: entry.defaultConfig.district ?? '',
    customSearchUrl: entry.defaultConfig.customSearchUrl ?? '',
    rssUrl: entry.defaultConfig.rssUrl ?? '',
    includeHouses: (entry.defaultConfig.includeHouses as SourceDraft['includeHouses']) ?? 'no',
    wgMode: (entry.defaultConfig.wgMode as SourceDraft['wgMode']) ?? 'rooms',
  };
}

export function getCatalogByMarket(market?: SourceMarket | 'ALL') {
  if (!market || market === 'ALL') return sourceCatalog;
  return sourceCatalog.filter((item) => item.market === market);
}

export const defaultSourceDrafts: SourceDraft[] = sourceCatalog.map((entry) => ({
  ...buildDraftFromCatalog(entry.id),
  name: `${entry.label} ${entry.defaultCity}`,
}));
